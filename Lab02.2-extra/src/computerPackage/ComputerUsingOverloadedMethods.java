package computerPackage;

public class ComputerUsingOverloadedMethods {

    int add(int x, int y){
        return x + y;
    }
    int subtract(int x, int y){

        return x-y;
    }
    long add(long x, long y) {
        return x + y;
    }
    long subtract(long x, long y) {
        return x - y;
    }
}
